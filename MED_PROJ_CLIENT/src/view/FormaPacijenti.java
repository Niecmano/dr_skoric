    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        DijalogPacijent dp = new DijalogPacijent(this, rootPaneCheckingEnabled, null);
        dp.setVisible(true);
        osveziTabelu();
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int izbor = tblPac.getSelectedRow();
        if(izbor==-1){
            JOptionPane.showMessageDialog(this,"Niste izabrali nijednog pacijenta!", 
                    "Greska",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int id = mtp.getPacijent(tblPac.getSelectedRow()).getSifraPac();
        Kontroler.getInstance().izbrisiPacijent(id);
        osveziTabelu();
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        Pacijent pac = tfPacijent.getSelectedPacijent();
        if(pac!=null){
              mtp = new ModelTabelePacijenti(Kontroler.getInstance().filtrirajPacijente(pac));
              tblPac.setModel(mtp);
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        int izbor = tblPac.getSelectedRow();
        if(izbor==-1){
            JOptionPane.showMessageDialog(this,"Niste izabrali nijednog pacijenta!", 
                    "Greska",JOptionPane.ERROR_MESSAGE);
            return;
        }
        DijalogPacijent dp = new DijalogPacijent(this, rootPaneCheckingEnabled, mtp.getPacijent(izbor));
        dp.setVisible(true);
        osveziTabelu();
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnIzvestajiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzvestajiActionPerformed
        int izbor = tblPac.getSelectedRow();
        if(izbor==-1){
            JOptionPane.showMessageDialog(this,"Niste izabrali nijednog pacijenta!", 
                    "Greska",JOptionPane.ERROR_MESSAGE);
            return;
        }
        FormaIzvestajiPacijenta fip = new FormaIzvestajiPacijenta(mtp.getPacijent(izbor));
        fip.setVisible(true);
    }//GEN-LAST:event_btnIzvestajiActionPerformed

